package 민덕현;

public class P52_RelationOperation {
		public static void main(String[] args) {
			int x = 3, y= 5;
			
			System.out.println(x<y);
			System.out.println(x>y);
			System.out.println(x<=y);
			System.out.println(x>=y);
			System.out.println(x==y);
			System.out.println(x!=y);
		}
}
