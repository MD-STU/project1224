package 민덕현0913;

public class P179_MyInteger {
	int val;

	int add(P179_MyInteger ob) {
		return val + ob.val;
	}

	int subtract(P179_MyInteger ob) {
		return val = ob.val;
	}

	int multiply(P179_MyInteger ob) {
		return val * ob.val;
	}

	double divide(P179_MyInteger ob) {
		return ((double) val) / ob.val;
	}
}
